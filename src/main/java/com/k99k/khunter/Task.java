/**
 * FIXME Task 异步操作,与Action同步操作相区别
 * @author keel
 *
 */
public class Task {

	/**
	 * 
	 */
	public Task() {
	}
	
	private int id;
	
	private String name;

	/**
	 * @return the id
	 */
	public final int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public final void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public final String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public final void setName(String name) {
		this.name = name;
	}
	
	

}
